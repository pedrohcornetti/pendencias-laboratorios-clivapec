// Monta a lista achatada com ordem sequencial (A, depois B, depois C).
export function seedFornecedores() {
  const arr = []
  let ordem = 1
  for (const classe of ['A', 'B', 'C']) {
    for (const nome of SEED[classe]) {
      arr.push({ nome, classe, ordem: ordem++ })
    }
  }
  return arr
}
